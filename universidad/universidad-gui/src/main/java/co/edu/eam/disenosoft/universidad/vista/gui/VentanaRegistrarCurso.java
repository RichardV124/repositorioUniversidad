    private void limpiarAsignatura(){
        tfcodigoasignatura.setText(null);
        tfnombreasignatura.setText(null);
        tfcreditosasignatura.setText(null);
        tfTipoAsignatura.setText(null);
        DefaultTableModel tabla = (DefaultTableModel) tablaHorario.getModel();
        tabla.setRowCount(0);
    }
    
    /**
     * Método para limpiar los campos de estudiante
     */
    private void limpiarEstudiante(){
        tfcedulaestudiante.setText(null);
        tfnombreestudiante.setText(null);
        tfcreditosregistradosestudiante.setText(null);
    }
    
    /**
     * Método para llenar la tabla con los datos de la asignatura
     * @param asignatura, la asignatura que se busca en los cursos
     * @throws Exception 
     */
    private void llenarTabla(Asignatura asignatura) throws Exception{
    	//Lista con los cursos que tiene esa asignatura
        List<Curso> cursos = controlador.listarCursoPorAsignatura(asignatura);
        DefaultTableModel tabla = (DefaultTableModel) tablaHorario.getModel();
        tabla.setRowCount(0);
        for (int i = 0; i < cursos.size(); i++) {
            String horario = "";
            for (int j = 0; j < cursos.get(i).getSesiones().size(); j++) {
                horario += cursos.get(i).getSesiones().get(j).getDia().toString() + " ";
                horario += cursos.get(i).getSesiones().get(j).getHoraInicio() + "-";
                horario += cursos.get(i).getSesiones().get(j).getHoraFinal() + ". ";
            }
            tabla.addRow(new Object[]{cursos.get(i).getCodigo(), horario});
        }
    }
