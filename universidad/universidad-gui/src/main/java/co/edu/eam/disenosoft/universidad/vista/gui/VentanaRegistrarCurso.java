    private void btnBuscarEstuainteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEstuainteActionPerformed
        // TODO add your handling code here:
         try {
            if (tfcedulaestudiante.getText() == null) {
                JOptionPane.showMessageDialog(null, "Ingrese la cédula del estudiante");
            } else {
                estudiante = controlador.buscarEstudiante(tfcedulaestudiante.getText());
                if (estudiante != null) {
                    tfnombreestudiante.setText(estudiante.getNombre() + " " + estudiante.getApellido());
                    tfcreditosregistradosestudiante.setText("" + controlador.contarCreditosEstudiante(estudiante));
                } else {
                    limpiarEstudiante();
                    JOptionPane.showMessageDialog(null, "No hay un estudiantes registrado "
                            + "con la cédula " + tfcedulaestudiante.getText());
                }

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnBuscarEstuainteActionPerformed

    private void btnBuscarAsignaturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarAsignaturaActionPerformed
        // TODO add your handling code here:
          try {
            if (tfcodigoasignatura.getText() == null) {
                JOptionPane.showMessageDialog(null, "Ingrese el código de la asignatura");
            } else {
                Asignatura asig = controlador.buscarAsignatura(tfcodigoasignatura.getText());
                if (asig != null) {
                    tfnombreasignatura.setText(asig.getNombre());
                    tfcreditosasignatura.setText(asig.getNumeroCreditos() + "");
                    tfTipoAsignatura.setText(asig.getTipo().toString());
                    llenarTabla(asig);
                } else {
                    limpiarAsignatura();
                    JOptionPane.showMessageDialog(null, "No hay una asignatura registrada "
                            + "con el código " + tfcodigoasignatura.getText());
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnBuscarAsignaturaActionPerformed

	private void btnRegistrarAsignatureaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnRegistrarAsignatureaActionPerformed
		try{
            if (tfcedulaestudiante.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor busque un estudiante");
                return;
            }
            DefaultTableModel tabla = (DefaultTableModel) tablaHorario.getModel();
            if (tablaHorario.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(null, "Seleccione un curso");
                return;
            }
            int row = tablaHorario.getSelectedRow();
            String codigo = (String) tabla.getValueAt(row, 0);            
            Curso curso = controlador.buscarCurso(codigo);
            controlador.crearRegistro(curso, estudiante);
            JOptionPane.showMessageDialog(null, "Se ha hecho el registro satisfactoriamente");
            btnBuscarEstuainteActionPerformed(evt);
        } catch(ExcepcionNegocio ex){
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
	}// GEN-LAST:event_btnRegistrarAsignatureaActionPerformed

